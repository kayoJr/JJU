			<h3 class="aligncenter">Educator Views of Incorporating a Constructivist Approach  to Early Childhood Education in Ethiopia</h3>
				<p class="aligncenter">
					Stephanie Shine, Texas Tech University, Yigzaw Haile & Beide Melaku, Jigjiga University

				</p>
				</div>
				<div class="span6">
				<h4>Aims</h4>
				<p>
				Developing countries invest in early childhood care and education programs to  promote school readiness and reduce poverty (Kabay, Wolf, & Yoshikawaa, 2017).
				The Ethiopian Ministry of Education has produced a Curriculum Framework  (2018) that incorporates active learning activities based on a constructivist  outlook (DeVries et al, 2002; Mills, 2007):

				</p>
				<p class="aligncenter testimonial"><strong>
					Children learn best when they are actively involved with materials,  ideas and other people, have opportunities or choice, and are  supported as they explore and talk about their findings.
					</strong>
				</p>
				<p>
					It is not known how well this Eurocentric perspective (Nsamenang, 2006, 2007)  will be accepted by teacher educators or preschool teachers in Ethiopia.

				</p>
				<h4>Goals</h4>
				<ol>
                  <li>To examine educator views of the challenges and cultural compatibility of  adopting a constructivist approach in preschools in Ethiopia</li>
                  <li>To examine educator strategies for implementation of this approach</li>
                  
                </ol>
				<h4>Method</h4>
					<img src="img/dummies/dummy-6.jpg" alt="" class="align-left" />
					<p>
					  " The constructivist approach is the best approach  to teach children. Because we have already  taught many children to know what we know and  to become like us. But we didn’t give them room  to bring out what is inside them, and we failed to  allow them to become who they really are. "
					</p>
					
					<span>
                       - Oman Lero, Teacher Educator
						</span>
						
						<div class="solidline">
                        </div>
                     
					 <h4>Participants</h4>
					 <p>
						43 College teacher educators from regions throughout Ethiopia who attended  a 9-day workshop on the HighScope constructivist curriculum in Adama <br>
						40 Male, 3 Female	M = 38.6 years old

							
					 </p>
					 <h4>Measures</h4>
					 <ol>
						<li>Assessment of the Constructivist Approach to Early Childhood Education in  Ethiopia (Shine, Haile & Melaku, 2018), a survey on the challenges and benefits of  implementing this method to teachers, parents, and children (12 educators)
						</li>
						<li>An end-of-course survey on introducing the constructivist curriculum to  preschool pre-service teachers (43 educators)
						</li>
					</ol>
				</div>
				<div class="span5">
					<h4>Results</h4>
					<p>
						Conceptual content analysis (Krippendorf, 1980; Weber, 1990), with intercoder  agreement rates of 90%, yielded the following themes:

					</p>
					<table class="table">
						  <tr>
						  <th>Theme</th>
						  <th>Operational Definition of Category</th>
						  </tr>
						  
						  <tr>
						  <td>Role of Teacher</td>
						  <td>Teachers see their role as directing children’s learning. Parents see teachers as disseminating information and children as  dependent on teachers.</td>
						  </tr>
						  
						  <tr>
						  <td>Nature of Learning</td>
						  <td>Learning is defined and measured by knowledge of facts. Play is not seen as leading to learning.</td>
						  </tr>
						  
						  <tr>
						  <td>Children’s Autonomy</td>
						  <td>Children are not accustomed to initiating communication at school.<br> Children need supports to engage in active learning.</td>
						  </tr>
						  
						  <tr>
						  <td>Burden on Teachers</td>
						  <td>Guiding children’s learning will be challenging to teachers. Teacher preparation to support active learning will be taxing.</td>
						  </tr>
						  
						  <tr>
						  <td>Implemen-tation  Supports</td>
						  <td>Investments in teacher training are needed.<br>
							A curriculum that integrates a constructivist outlook is necessary. <br> Classroom materials are required to support a constructivist curriculum.  <br>Government buy-in is critical to adopt a constructivist approach.</td>
						  </tr>
						  
						
						  
						</table>
						
