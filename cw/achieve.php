						<h4>
						Conclusions
						</h4>
						<p>
							Ethiopian educators identified challenges and strategies to adopting a  constructivist approach in early childhood classrooms:
						</p>
						<ul>
							<li>Beliefs about teachers as knowledge brokers and children as recipients of  knowledge are deeply rooted in Ethiopia. </li>
							<li>Mass awareness of the benefits of the constructivist approach, government  commitment, teacher, parent, and administrator training, and integrated  curriculum design are proposed for successful implementation. </li>
							
						</ul>
