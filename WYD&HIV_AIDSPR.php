			<div class="row">
					
					<h6>Main Attainments this year</h6>
					<p>With the commitment of the new leadership, eight female instructors were appointed to middle and lower level management this Academic Year. </p>
					<p>The Directorate in collaboration with DRPTT has organized the first female instructors’ research proposal defense workshop for 30 female researchers.</p>
					
					<p>Welcoming event for 1500 newly enrolled female students was organized which helps them to familiarize themselves with the campus environment. In this event, around 34 outstanding senior female students were awarded to motivate and share experiences to new students. </p>
					<p>Life skill training was given for 2000 new students during their enrollment to make them prepared to the challenges they may face during their stay in the campus.</p>
					<p>Since university students are youth and at high risk of facing sexual and reproductive health issues, the Directorate provided three trainings on sexual, reproductive health and risk behaviors for 690 students in collaboration with IFHP and Family Guidance.</p>
					<p>To keep our students from peer pressure, different events like ‘pajama nights’ program and life talk shows were organized. These events were made in three sessions around students’ dormitories in various topics which were helpful in preventing risky behaviors.</p>
					<p>The Office was also responsible to Celebrate World HIV/AIDS Day and World Disability Day. In these days, a presentation on current situation regarding HIV/AIDS and awarding handicapped and best performing students were done respectively.</p>
					<p>During the International Women’s Day (March 8) event, 11 best performing supportive staffs were awarded to motivate female staffs. In addition to that, 30 female and male lecturers who were volunteers in providing tutorial classes for students were given certificate of recognition. Based on the principle of zero tolerance for sexual harassment, by that Day the Directorate distributed 2000 Amharic version copies of the Anti-Sexual Harassment and Misconduct Policy which was adopted from the Federal Ministry of Education to introduce the policy to the campus community.</p>
					<p>Female instructors’ Code of Conduct was launched this Academic Year with the aim of helping female instructors and students in various aspects. </p>
					<p>The Office also offered some services to the handicapped students; arranging suitable conditions for their learning process, supporting economically, and providing laundry services for those who are seriously disabled. </p>
					<p>543 students were supported financially, and provided with hygiene & sanitation items, learning materials, graduation clothes, photo copy and transportation costs with the help of different partners.</p>
					<p>This Academic Year, our office also established a laundry service for all students in a lower price. The income of this service is spent for those students who need support from the Office.</p>
					<p>The Directorate disseminated 2 minutes explanation power point note about HIV/AIDS for all colleges to transmit the messages to their students about HIV/AIDs and also using the campus mini media. In line with that, condom boxes were erected around 5 areas.</p>
					<p>There is a plan together with the President Office to employ outstanding female graduate students of this year for the coming Academic Year. </p>
					<p>Finally, the Directorate would like to say these achievements are made with the great contributions of the top leadership of JJU, the University community, and different stakeholders. </p>
			
